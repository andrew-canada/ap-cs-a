int mc(int *arr, int n)
{
    for (int i = 0; i < n; i++)
    {
        dp[i][i + 1] = 0;
    }
    int r;
    for (int len = 2; len < n; len++)
    {
        for (int l = 0; l < n - len; l++)
        {
            r = l + len;
            dp[l][r] = INF;
            for (int i = l + 1; i < r; i++)
            {
                dp[l][r] = min(dp[l][r], dp[l][i] + dp[i][r] + arr[l] * arr[i] * arr[r]);
            }
        }
    }
    return dp[0][n - 1];
}