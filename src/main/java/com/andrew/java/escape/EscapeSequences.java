package com.andrew.java.escape;

public class EscapeSequences {

	public static void main(String[] args) {
		String name = "Andrew";
		System.out.println("Hello Andrew");
		
		System.out.println("escape \" in string with a \\: e.g. Hello \"Andrew\"");
		System.out.println("escape \\ in string with a \\: e.g. c:\\work\\projects");
		System.out.println("create new line in string with a \\n: e.g. Hello\n \"Andrew\"");
		System.out.println("create tab in string with a \\t: e.g. Hello\t\"Andrew\"");

	}

}
