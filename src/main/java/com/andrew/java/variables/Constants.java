package com.andrew.java.variables;

public class Constants {

	public static void main(String[] args) {
		int a = 5;
		a = 6;
		System.out.println(a);
		// keyword final means can't change value
		final int B = 7;
		// can't assign a new value for a constant
		// B = 9;
		System.out.println(B);

	}

}
